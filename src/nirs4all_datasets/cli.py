@app.command()
def bootstrap(
    source_tree: Path,
    root: Path = typer.Option(Path("."), help="Registry root."),
    force: bool = typer.Option(False, help="Overwrite managed descriptors even when unchanged."),
    prune: bool = typer.Option(False, help="Re-base: delete managed descriptors no longer in the source (writes catalog/reconciliation.json)."),
) -> None:
    """Auto-generate schema-2.0 descriptors for every v2.0 package under SOURCE_TREE (``<tree>/v2.0/*``)."""
    from nirs4all_datasets.bootstrap import bootstrap as run_bootstrap
    from nirs4all_datasets.catalog import build_catalog

    report = run_bootstrap(source_tree, root, force=force, prune=prune)
    typer.echo(
        f"bootstrap: created={len(report['created'])} updated={len(report['updated'])} skipped={len(report['skipped'])} "
        f"removed={len(report.get('removed', []))} {'(pruned)' if report.get('pruned') else '(kept)'} errors={len(report['errors'])}"
    )
    for err in report["errors"][:10]:
        typer.echo(f"  ERROR {err['leaf']}: {err['error']}", err=True)
    build_catalog(root)


@app.command()
def add(source: Path, dataset_id: str, root: Path = typer.Option(Path("."), help="Registry root.")) -> None:
    """Convert raw SOURCE into datasets/<id> (canonical), build its card, and refresh the catalog."""
    from nirs4all_datasets.catalog import build_catalog
    from nirs4all_datasets.organize import organize
    from nirs4all_datasets.qualify.profile import card_metadata_fresh, qualify

    descriptor = _descriptor(root, dataset_id)
    result = organize(source, descriptor, root / "datasets")
    typer.echo(f"organize: {'skipped (unchanged)' if result.skipped else 'processed'} -> {result.dataset_dir}")
    if not result.skipped or not card_metadata_fresh(result.dataset_dir, descriptor):
        qualify(result.dataset_dir, descriptor)
        typer.echo("card built (card.json + card.md + croissant.json)")
    build_catalog(root)


@app.command()
def qualify(
    dataset_id: str,
    root: Path = typer.Option(Path("."), help="Registry root."),
    anonymize: bool = typer.Option(False, "--anonymize", help="Also write card.anon.json (masked names, normalized Y) for the anonymized tier."),
) -> None:
    """(Re)build a dataset's identity card (card.json + card.md + croissant.json) from its canonical data."""
    from nirs4all_datasets.qualify.profile import qualify as run_qualify

    descriptor = _descriptor(root, dataset_id)
    dataset_dir = root / "datasets" / dataset_id
    card = run_qualify(dataset_dir, descriptor)
    typer.echo(f"card: datasets/{dataset_id}/card.json (+ card.md, croissant.json)")
    if anonymize:
        from nirs4all_datasets.qualify.anonymize import anonymize_card

        (dataset_dir / "card.anon.json").write_text(json.dumps(anonymize_card(card), indent=2, sort_keys=True), encoding="utf-8")
        typer.echo(f"anonymized card: datasets/{dataset_id}/card.anon.json")


@app.command("build-all")
def build_all_cmd(
    source_tree: Path = typer.Option(..., help="Read-only source tree the descriptors were generated from."),
    root: Path = typer.Option(Path("."), help="Registry root."),
    workers: int | None = typer.Option(None, help="Parallel workers (default: CPU-2, capped at 6)."),
    only: str | None = typer.Option(None, help="Comma-separated dataset ids to (re)build."),
    skip_assets: bool = typer.Option(False, help="Skip plot rendering (faster cards)."),
    force: bool = typer.Option(False, help="Rebuild canonical + cards even when unchanged."),
    protocol_refresh: bool = typer.Option(False, "--protocol-refresh", help="Re-qualify (rebuild cards) without rebuilding canonical bytes."),
    site: bool = typer.Option(False, help="Build the static site afterwards."),
) -> None:
    """Organize + qualify all datasets (parallel, failure-isolated), then refresh the catalog."""
    from nirs4all_datasets.bulk import build_all, write_report
    from nirs4all_datasets.catalog import build_catalog

    only_ids = [s.strip() for s in only.split(",") if s.strip()] if only else None

    def _progress(done: int, total: int, rec: dict) -> None:
        mark = {"ok": "✓", "partial": "~", "skipped": "·", "failed": "✗"}.get(rec["status"], "?")
        suffix = f"  ({rec.get('reason')})" if rec["status"] == "failed" else ""
        typer.echo(f"[{done}/{total}] {mark} {rec['id']}{suffix}")

    report = build_all(root, source_tree, workers=workers, only=only_ids, skip_assets=skip_assets, force=force, protocol_refresh=protocol_refresh, progress=_progress)
    write_report(report, root / "bulk_report.json")
    typer.echo(f"build-all: {report['counts']} -> {root / 'bulk_report.json'}")
    build_catalog(root)
    if site:
        from nirs4all_datasets.site import build_site

        typer.echo(f"site: {build_site(root, root / 'site')}")


@app.command("health-check")
def health_check_cmd(
    root: Path = typer.Option(Path("."), help="Registry root."),
    timeout: float = typer.Option(10.0, help="Per-origin HTTP timeout (seconds)."),
    only: str | None = typer.Option(None, help="Comma-separated dataset ids to probe."),
) -> None:
    """Probe every dataset's open origin sources and write catalog/health.json (flags degraded public datasets)."""
    from nirs4all_datasets.catalog import build_catalog
    from nirs4all_datasets.health import run_health_check

    only_ids = [s.strip() for s in only.split(",") if s.strip()] if only else None
    report = run_health_check(root, timeout=timeout, only=only_ids)
    typer.echo(f"health-check: {report['n_datasets']} probed, {report['n_degraded']} degraded -> catalog/health.json")
    build_catalog(root)


