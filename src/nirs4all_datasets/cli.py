@app.command()
def publish(
    dataset_id: str,
    collection: str | None = typer.Option(None, help="Dataverse collection alias (required for the first publication)."),
    contact_email: str | None = typer.Option(None, help="Dataset contact email (required for the first publication)."),
    root: Path = typer.Option(Path("."), help="Registry root."),
    instance: str | None = typer.Option(None, help="Dataverse instance URL override."),
) -> None:
    """Publish (first time) or update (new version) a dataset on Dataverse, governance-gated.

    Private/anonymized datasets upload with access-restricted files. On success the minted DOI + version
    are written into the descriptor, the manifest records the Dataverse file ids + native checksums (so
    token access works), and the card/Croissant + catalog are refreshed.
    """
    from nirs4all_datasets.catalog import build_catalog
    from nirs4all_datasets.publish import publish_dataset, record_publication, update_dataset
    from nirs4all_datasets.qualify.profile import qualify as run_qualify

    descriptor = _descriptor(root, dataset_id)
    dataset_dir = root / "datasets" / dataset_id
    client = _dataverse_client(instance)

    if descriptor.dataverse.doi:
        result = update_dataset(descriptor, dataset_dir, client)
        action = "updated"
    else:
        if not collection or not contact_email:
            raise typer.BadParameter("--collection and --contact-email are required for a first publication.")
        result = publish_dataset(descriptor, dataset_dir, client, collection=collection, contact_email=contact_email)
        action = "published"

    record_publication(dataset_dir, client, result["doi"], result.get("version"))
    _save_descriptor_dataverse(root, dataset_id, result["doi"], result.get("version"))
    run_qualify(dataset_dir, _descriptor(root, dataset_id))  # reload with the DOI so card/Croissant carry it
    build_catalog(root)
    typer.echo(f"{action} {dataset_id}: DOI {result['doi']} v{result.get('version')} ({result['files']} files)")


@app.command()
def grant(
    dataset_id: str,
    to: str = typer.Option(..., help="Assignee: '@username' or '&groupAlias'."),
    role: str = typer.Option("fileDownloader", help="Dataverse role alias (e.g. fileDownloader)."),
    root: Path = typer.Option(Path("."), help="Registry root."),
    instance: str | None = typer.Option(None, help="Dataverse instance URL override."),
) -> None:
    """Grant a role on a published dataset (so a user/group can download its restricted files)."""
    descriptor = _descriptor(root, dataset_id)
    if not descriptor.dataverse.doi:
        raise typer.BadParameter(f"{dataset_id} has no DOI yet (publish it first).")
    client = _dataverse_client(instance)
    db_id = client.dataset_db_id(_pid(descriptor.dataverse.doi))
    assignment = client.assign_role(db_id, to, role)
    typer.echo(f"granted {role} to {to} on {dataset_id} (assignment {assignment.get('id')})")


@app.command()
def revoke(
    dataset_id: str,
    to: str = typer.Option(..., help="Assignee to revoke: '@username' or '&groupAlias'."),
    root: Path = typer.Option(Path("."), help="Registry root."),
    instance: str | None = typer.Option(None, help="Dataverse instance URL override."),
) -> None:
    """Revoke a user's/group's role assignment(s) on a published dataset."""
    descriptor = _descriptor(root, dataset_id)
    if not descriptor.dataverse.doi:
        raise typer.BadParameter(f"{dataset_id} has no DOI yet.")
    client = _dataverse_client(instance)
    db_id = client.dataset_db_id(_pid(descriptor.dataverse.doi))
    matches = [a for a in client.list_assignments(db_id) if a.get("assignee") == to]
    if not matches:
        typer.echo(f"no assignment for {to} on {dataset_id}", err=True)
        raise typer.Exit(1)
    for assignment in matches:
        client.delete_assignment(db_id, int(assignment["id"]))
    typer.echo(f"revoked {len(matches)} assignment(s) for {to} on {dataset_id}")


@app.command()
def restrict(
    dataset_id: str,
    off: bool = typer.Option(False, "--off", help="Un-restrict instead of restrict."),
    root: Path = typer.Option(Path("."), help="Registry root."),
    instance: str | None = typer.Option(None, help="Dataverse instance URL override."),
) -> None:
    """Restrict (or --off to un-restrict) all of a published dataset's files, then publish a new version."""
    descriptor = _descriptor(root, dataset_id)
    if not descriptor.dataverse.doi:
        raise typer.BadParameter(f"{dataset_id} has no DOI yet.")
    client = _dataverse_client(instance)
    pid = _pid(descriptor.dataverse.doi)
    file_ids = [info["file_id"] for info in client.file_checksums(pid).values() if info.get("file_id") is not None]
    for file_id in file_ids:
        client.restrict_file(int(file_id), restrict=not off)
    result = client.publish_dataset(pid, version_type="minor")
    client.wait_for_indexing(pid)
    from nirs4all_datasets.publish import record_publication

    record_publication(root / "datasets" / dataset_id, client, descriptor.dataverse.doi, result.get("versionNumber"))
    _save_descriptor_dataverse(root, dataset_id, descriptor.dataverse.doi, result.get("versionNumber"))
    typer.echo(f"{'un-restricted' if off else 'restricted'} {len(file_ids)} file(s) on {dataset_id}; published v{result.get('versionNumber')}")


