@app.command("list")
def list_cmd(
    root: Path = typer.Option(Path("."), help="Registry root."),
    tier: str | None = typer.Option(None, help="Filter by tier (public|private|anonymized)."),
    domain: str | None = typer.Option(None, help="Filter by domain."),
    spectro_family: str | None = typer.Option(None, help="Filter by spectroscopy family (NIR|MIR|Raman|...)."),
) -> None:
    """List catalog datasets (optionally filtered)."""
    from nirs4all_datasets.catalog import search

    entries = search(root, tier=tier, domain=domain, spectro_family=spectro_family)
    if not entries:
        typer.echo("(no datasets)")
        return
    for e in entries:
        typer.echo(f"{e['id']:<34} {e['tier']:<10} {e.get('spectro_family',''):<6} src={e.get('n_sources')} Y={e.get('n_targets')} n={e.get('n_samples')} doi={e.get('doi')}")


@app.command()
def card(dataset_id: str, root: Path = typer.Option(Path("."), help="Registry root.")) -> None:
    """Print a dataset's identity card (JSON)."""
    from nirs4all_datasets.catalog import get_card

    data = get_card(root, dataset_id)
    if data is None:
        typer.echo(f"no card for {dataset_id!r} (run: n4a-datasets qualify {dataset_id})", err=True)
        raise typer.Exit(1)
    typer.echo(json.dumps(data, indent=2))


