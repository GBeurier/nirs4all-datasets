"""Command-line interface (``n4a-datasets``).

Thin orchestration over the library modules. Heavy dependencies (nirs4all, matplotlib, requests) are
imported lazily inside the commands that need them, so ``--help``, ``catalog``, ``list`` and ``card``
work in a minimal environment.
"""
from __future__ import annotations

import json
from pathlib import Path

import typer
import yaml

app = typer.Typer(add_completion=False, help="Store, qualify, and serve RAW NIRS reference datasets.")


def _descriptor(root: Path, dataset_id: str):  # noqa: ANN202
    from nirs4all_datasets.schema import DatasetDescriptor

    path = root / "catalog" / "datasets" / f"{dataset_id}.yaml"
    if not path.exists():
        raise typer.BadParameter(f"no descriptor for {dataset_id!r} at {path}")
    return DatasetDescriptor(**(yaml.safe_load(path.read_text(encoding="utf-8")) or {}))


def _pid(doi: str) -> str:
    """Dataverse API persistentId form (``doi:<suffix>``)."""
    return doi if doi.startswith("doi:") else f"doi:{doi}"


def _dataverse_client(instance: str | None):  # noqa: ANN202
    """Build a token-authenticated Dataverse client from resolved settings."""
    from nirs4all_datasets.config import get_settings
    from nirs4all_datasets.dataverse import DataverseClient

    settings = get_settings(instance=instance)
    return DataverseClient(settings.instance, token=settings.require_token())


def _save_descriptor_dataverse(root: Path, dataset_id: str, doi: str, dataset_version: object) -> None:
    """Write the minted DOI + version into the descriptor YAML (preserving field order + the auto banner)."""
    path = root / "catalog" / "datasets" / f"{dataset_id}.yaml"
    data = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    dv = data.setdefault("dataverse", {})
    dv["doi"] = doi[4:] if doi.startswith("doi:") else doi
    if dataset_version is not None:
        dv["dataset_version"] = str(dataset_version)
    banner = "# AUTO-GENERATED by nirs4all-datasets `bootstrap`. Edit freely; set generation.managed: false to protect from regeneration.\n" if (data.get("generation") or {}).get("managed") else ""
    path.write_text(banner + yaml.safe_dump(data, sort_keys=False, allow_unicode=True), encoding="utf-8")


