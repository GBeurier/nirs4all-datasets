//! Drift guard: the committed ABI snapshots and the generated header must list
//! exactly the public `n4ds_*` surface. The `abi-check` CI additionally diffs the
//! *built* cdylib's exported symbols against these snapshots on each platform.

use std::collections::BTreeSet;
use std::path::Path;

/// The canonical public C ABI surface (source of truth for this test).
const EXPECTED: &[&str] = &[
    "n4ds_abi_version",
    "n4ds_check_abi_compatibility",
    "n4ds_context_create",
    "n4ds_context_destroy",
    "n4ds_context_last_error",
    "n4ds_fetch",
    "n4ds_prepare_raw",
    "n4ds_resolve",
    "n4ds_retrieve_raw",
    "n4ds_string_free",
    "n4ds_verify_cached",
];

fn snapshot(name: &str) -> BTreeSet<String> {
    let path = Path::new(env!("CARGO_MANIFEST_DIR")).join("abi").join(name);
    std::fs::read_to_string(path)
        .unwrap_or_default()
        .lines()
        .map(str::trim)
        .filter(|l| !l.is_empty())
        .map(str::to_string)
        .collect()
}

#[test]
fn snapshots_match_the_canonical_surface() {
    let expected: BTreeSet<String> = EXPECTED.iter().map(|s| s.to_string()).collect();
    for file in [
        "expected_symbols_linux.txt",
        "expected_symbols_macos.txt",
        "expected_symbols_windows.txt",
    ] {
        assert_eq!(
            snapshot(file),
            expected,
            "{file} drifted from the canonical surface"
        );
    }
}

#[test]
fn every_symbol_is_n4ds_prefixed() {
    assert!(
        EXPECTED.iter().all(|s| s.starts_with("n4ds_")),
        "every exported symbol must start with n4ds_"
    );
}

#[test]
fn committed_header_declares_every_symbol() {
    let header = Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("include")
        .join("nirs4all_datasets.h");
    // The header is generated by build.rs; skip if a packager stripped it.
    if let Ok(text) = std::fs::read_to_string(&header) {
        for sym in EXPECTED {
            assert!(text.contains(sym), "generated header is missing {sym}");
        }
    }
}
